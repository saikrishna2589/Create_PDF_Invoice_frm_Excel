user_prompt = "Enter a to-do:"

todo_list = []

while True:  # while is followed by conditional statement
    todo = input(user_prompt)
    todo = todo.capitalize()  # .capitalise is  a specific method (attribute) for string object
    todo_list.append(todo)  # .append is coming of the list here. here using '.append' is a method'
    # methods are similar to functions in that it has name parenthesis and input argument.
    # where they differ is methods are attached to data types i.e specific to list function in this case
    print(todo_list)


